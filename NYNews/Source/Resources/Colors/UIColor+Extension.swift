import UIKit

extension UIColor {

    /// white Color
    public class var whiteColor: UIColor {
        return UIColor(named: "whiteColor")!
    }
    
    /// black Color
    public class var blackColor: UIColor {
        return UIColor(named: "blackColor")!
    }
    
    /// gray Color
    public class var grayColor: UIColor {
        return UIColor.gray
    }
    
    /// lightGray Color
    public class var lightGrayColor: UIColor {
        return UIColor.lightGray
    }
    
}
